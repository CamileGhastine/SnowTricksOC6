    public function findByCategory($id)
    {
        return $this->createQueryBuilder('t')
            ->join('t.categories', 'c')
            ->where('c.id = :id')
            ->setParameter('id', $id)
            ->orderBy('t.updatedAt', 'DESC')
            ->getQuery()
            ->getResult()
            ;

